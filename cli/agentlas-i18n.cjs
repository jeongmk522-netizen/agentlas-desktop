function t(lang, key, ...args) {
  const table = STRINGS[lang] || STRINGS.en;
  let s = table[key];
  if (s == null) s = STRINGS.en[key];
  if (s == null) return key;
  let i = 0;
  return s.replace(/%s/g, () => (i < args.length ? String(args[i++]) : "%s"));
}

